                <p class="detil">5. 由冷藏(冷凍)庫內取出茶葉時，應先讓茶罐內茶葉溫度回升至與室溫相近，才可取出茶葉，否則驟予打開茶罐，茶葉容易凝結水氣而增加含水量，使未泡完之茶葉加速劣變。</p>

