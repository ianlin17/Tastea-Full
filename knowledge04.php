                <p class="detil">購買茶葉後不要以為直接放在袋中就可以了，這樣茶葉會很快的失去原先的滋味喔！提供一些簡單的小方法來讓你買回來的茶葉延長新鮮度。如分袋保存，利用市售的拉鏈式食物保鮮袋、鋁箔袋將茶以小袋分將後擠出空氣、封口，再取另一個反向套入、排氣、封口放置乾燥陰涼處保存。或者利用乾淨沒生鏽的金屬罐或密封罐先以一小撮乾茶葉放在罐裡上下搖晃去味，倒掉後再放入茶葉保存。
                    由於茶葉為極乾燥的產品，開封後碰到空氣、光線、溫度、濕度後，滋味就會漸漸消失，所以不管用何種方法保存茶葉都一定要放在陰涼乾燥的地方。而一般家庭選購的茶葉多為罐裝或散裝茶，由於買回後不是一次泡完，所以就會遭遇到眝存的問題，茶葉保存可依溫度不同分為常溫及低溫兩種方式，以下建議幾種家庭常用的茶葉眝存方法：

                </p>
