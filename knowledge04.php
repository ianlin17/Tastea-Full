            <div class="info col-lg-6">
                <p class="detil">一、常溫保存-塑膠袋、鋁箔袋貯存法：</p><br>
                <p class="detil line">常溫係指保存空間的溫度，隨著氣溫而不斷變動。一般短期保存（一個月以內）或中下級茶葉，以常溫保存即可。另外，使用袋或罐裝保存時，希望效果更好可在外袋或罐內放包好的乾燥劑，並定時更換。但高級煎茶、龍井或碧螺春等無論保存期長短，建議以低溫保存來保持色澤與新鮮味。</p>

                <p class="detil">1.塑膠袋、鋁箔袋貯存法：
                    最好選有封口且為裝食品用之塑膠袋，材料厚實一點、密度高的較好，不要用有味道或再製的塑膠袋。裝入茶後袋中空氣應盡量擠出，如能用第二個塑膠袋反向套上則更佳，以透明塑膠袋裝茶後不宜照射陽光。以鋁箔袋裝茶原理與塑膠袋類同。另外，將買回來的茶分袋包裝，密封後裝置於冰箱內，然後分批沖泡，以減少茶葉開封後與空氣接觸的機會，延緩品質劣變的產生。
                </p>
            </div>
            <div class="info col-lg-6">
                <p class="detil">二、常溫保存-金屬罐貯存法：</p><br>
                <p class="detil line">2.金屬罐貯存法：
                    可選用鐵罐、不銹鋼罐或質地密實的錫罐，如果是新買的罐子，或原先存放過其他物品留有味道的罐子，可先用少許茶末置於罐內，蓋上蓋子，上下左右搖晃輕擦罐壁後倒棄，以去除異味。市面上有販售兩層蓋子的不銹鋼茶罐，簡便而實用，如能配合以清潔無味之塑膠袋裝茶後，再置入罐內蓋上蓋子，以膠帶黏封蓋口則更佳。裝有茶葉的金屬罐應置於陰涼處，不要放在陽光直設、有異味、潮溼、有熱源的地方，如此，鐵罐才不易生銹，亦可減緩茶葉陳化、劣變的速度。另錫罐材料緻密，對防潮、防氧化、阻光、防異味有很好的效果。

                </p>
