                <p class="detil">三、低溫保存</p><br>
                <p class="detil">將茶葉貯存的環境保持在5度C以下，也就是使用冷藏庫或冷凍庫保存茶葉，使用此法應注意：</p>

                <p class="detil">1. 貯存期六個月以內者，冷藏溫度以維持0~5度C最經濟有效；貯藏期超過半年者，以冷凍(-10至-18度C)較佳。</p>
                <p class="detil">2. 貯茶以專用冷藏(冷凍)庫最好，如必須與其他食物共冷藏(凍)，則茶葉應妥善包裝，完全密封以免吸附異味。</p>
                <p class="detil">3. 茶葉的排列必須整齊，使冷空氣的循環達到冷卻效果。</p>
